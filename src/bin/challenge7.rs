extern crate cryptopals;

use cryptopals::aes;
use std::fs::File;
use std::io::prelude::*;

fn main() {
    println!("Challenge - 7... ");

    let filepath = "resources/challenge7/file.txt";

    let mut file = File::open(filepath).unwrap();
    let mut contents = String::new();
    let result = file.read_to_string(&mut contents);

    if result.is_err() {
        return;
    }

    // key expansion test
    let key1 = vec![
        0x2b, 0x7e, 0x15, 0x16, 0x28, 0xae, 0xd2, 0xa6, 0xab, 0xf7, 0x15, 0x88, 0x09, 0xcf, 0x4f,
        0x3c,
    ];
    let input1 = vec![
        0x32, 0x43, 0xf6, 0xa8, 0x88, 0x5a, 0x30, 0x8d, 0x31, 0x31, 0x98, 0xa2, 0xe0, 0x37, 0x07,
        0x34,
    ];
    let expected1: Vec<u8> = vec![
        0x39, 0x25, 0x84, 0x1d, 0x02, 0xdc, 0x09, 0xfb, 0xdc, 0x11, 0x85, 0x97, 0x19, 0x6a, 0x0b,
        0x32,
    ];
    assert_eq!(expected1, aes::cipher(&input1, &key1));
    assert_eq!(input1, aes::decipher(&expected1, &key1));

    // algo test
    let input2 = vec![
        0x00, 0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77, 0x88, 0x99, 0xaa, 0xbb, 0xcc, 0xdd, 0xee,
        0xff,
    ];
    let key2 = vec![
        0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0xa, 0xb, 0xc, 0xd, 0xe, 0xf,
    ];
    let expected2: Vec<u8> = vec![
        0x69, 0xc4, 0xe0, 0xd8, 0x6a, 0x7b, 0x04, 0x30, 0xd8, 0xcd, 0xb7, 0x80, 0x70, 0xb4, 0xc5,
        0x5a,
    ];
    println!("======================= CIPHER 2");
    assert_eq!(expected2, aes::cipher(&input2, &key2));
    println!("======================= DECIPHER 2");
    assert_eq!(input2, aes::decipher(&expected2, &key2));
    println!("Done!");
}
